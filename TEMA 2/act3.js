//Actividad 3. ¿Qué valor producirán las siguientes expresiones?

//a. parseInt("15.5");
//b. parseInt(true);
//c. parseInt("Pedro");
//d. parseInt(15 + "Pedro");
//e. parseFloat("15.5");

console.log(parseInt("15.5"));  // --> 15

console.log(parseInt(true));  // --> NaN

console.log(parseInt("Pedro"));  // --> NaN

console.log(parseInt(15 + "Pedro"));  // --> 15

console.log(parseFloat("15.5"));  // --> 15.5